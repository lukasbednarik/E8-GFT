def enumerate_class_one_via_helgason(M, Lambda_sq: float,
                                      verbose: bool = False) -> list[dict]:
    """All K-spherical highest weights λ = m_1 ω_1 + m_2 ω_6 + m_3 (2ω_7)
    + m_4 (2ω_8) with C_2(λ) ≤ Λ².

    Returns list of dicts sorted by C_2 ascending, with keys:
      'label_dynkin', 'lam', 'C2', 'd_lam', 'm_decomp' = (m_1, m_2, m_3, m_4).
    """
    omega = M["omega"]
    rho = M["rho"]
    pos_roots = M["pos_roots"]

    # Determine bounding box for (m_1, m_2, m_3, m_4) ∈ ℤ_≥0^4 with
    # C_2(λ) ≤ Λ².  Single-fundamental contribution gives lower bound:
    # C_2(m_i ω̃_i) = m_i² |ω̃_i|² + 2 m_i ⟨ω̃_i, ρ⟩ ≤ Λ², so we solve
    # the quadratic for each i to get M_i.
    fund_R8 = [hw_in_R8(label, omega) for label in SPHERICAL_FUND_DYNKIN]

    M_max = []
    for w in fund_R8:
        a = float(w @ w)
        b = 2.0 * float(w @ rho)
        if a < 1e-12:
            M_max.append(0)
            continue
        disc = b * b + 4.0 * a * Lambda_sq
        m_i = int(math.floor((-b + math.sqrt(disc)) / (2.0 * a) + 1e-9))
        M_max.append(max(m_i, 0))

    if verbose:
        print(f"    Bounding box for (m_1, m_2, m_3, m_4):  "
              f"M_max = {M_max}, total candidates = "
              f"{int(np.prod([m+1 for m in M_max])):,}")

    candidates = []
    for m1 in range(M_max[0] + 1):
        for m2 in range(M_max[1] + 1):
            for m3 in range(M_max[2] + 1):
                for m4 in range(M_max[3] + 1):
                    lam = (m1 * fund_R8[0] + m2 * fund_R8[1]
                           + m3 * fund_R8[2] + m4 * fund_R8[3])
                    C2 = casimir(lam, rho)
                    if C2 > Lambda_sq + 1e-9:
                        continue
                    # Reconstruct Dynkin label
                    dynkin = (m1, 0, 0, 0, 0, m2, 2 * m3, 2 * m4)
                    d_lam = int(round(weyl_dim(lam, rho, pos_roots)))
                    candidates.append({
                        "label_dynkin": dynkin,
                        "lam": lam,
                        "C2": float(C2),
                        "d_lam": d_lam,
                        "m_decomp": (m1, m2, m3, m4),
                    })

    candidates.sort(key=lambda c: (c["C2"], c["d_lam"]))
    return candidates


