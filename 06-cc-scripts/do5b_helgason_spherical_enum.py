def partial_log_det_naive(cands: list[dict]) -> float:
    """log det'_N|_naive(-Δ_EIX) := -ζ'_N(0)|_naive
        = -Σ_{C_2(λ) > 0} d_λ log C_2(λ)

    over class-one reps (m_λ = 1 by Helgason).  Excludes trivial rep
    (= zero mode subtraction).
    """
    val = 0.0
    for c in cands:
        if c["C2"] <= 1e-12:
            continue
        val -= c["d_lam"] * math.log(c["C2"])
    return -val  # = +Σ d_λ log C_2(λ)


