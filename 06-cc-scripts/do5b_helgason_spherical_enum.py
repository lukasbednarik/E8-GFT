def is_class_one_helgason(label_dynkin) -> bool:
    """Return True iff λ = Σ n_i ω_i is K-spherical for K = E_7 × SU(2)
    inside E_8, by the Cartan–Helgason criterion.

    For the symmetric pair (E_8, E_7×SU(2)) with reduced restricted root
    system F_4, the K-spherical highest weights form the monoid

        Λ_sph = ℤ_≥0 · {ω_1, ω_6, 2ω_7, 2ω_8}.

    Equivalently, in standard E_8 Dynkin labels (n_1, ..., n_8):

        n_2 = n_3 = n_4 = n_5 = 0   ∧   n_7, n_8 ∈ 2 ℤ_≥0,

    while n_1, n_6 are arbitrary non-negative integers.
    """
    n = list(label_dynkin)
    if any(n[i] != 0 for i in (1, 2, 3, 4)):
        return False
    if n[6] % 2 != 0 or n[7] % 2 != 0:
        return False
    return all(x >= 0 for x in n)


def helgason_decomposition(label_dynkin):
    """If λ is K-spherical, return (m_1, m_2, m_3, m_4) ∈ ℤ_≥0^4 such
    that λ = m_1 ω_1 + m_2 ω_6 + m_3 (2ω_7) + m_4 (2ω_8).  Otherwise
    return None.
    """
    if not is_class_one_helgason(label_dynkin):
        return None
    n = list(label_dynkin)
    return (n[0], n[5], n[6] // 2, n[7] // 2)


