def _setup_globals(verbose: bool = True):
    f_idx, f_val = load_structure_constants_numpy(str(CONSTANTS_PATH))
    pos_canonical = extract_pos_roots_numpy(f_idx, f_val)
    e7_basis, su2_basis, m_basis, _ = e7_su2_embedding(pos_roots=pos_canonical)
    h_basis = np.vstack([e7_basis, su2_basis])
    V_A, r_sq = build_VA()

    all_roots = np.concatenate([pos_canonical, -pos_canonical], axis=0)
    m_plus = m_plus_for_EIX(pos_canonical)
    m_pos_idx_in_canonical = m_plus_indices_in_canonical_pos(m_plus, pos_canonical)
    rl_set = root_set(all_roots)

    if verbose:
        print(f"        |Φ| = {len(all_roots)}, |Δ(𝔪⁺)| = {len(m_plus)}")
        print(f"        h_basis = {h_basis.shape}, m_basis = {m_basis.shape}")
        print(f"        V_A in Cartan(SU(2)): r_*² = κ(V_A,V_A) = {r_sq:.4f}")

    if verbose:
        print(f"\n[setup] Building strong-orthogonality compatibility matrix "
              f"({len(m_plus)}×{len(m_plus)})...", flush=True)
    t0 = time.time()
    compat = build_compatibility_matrix(m_plus, rl_set)
    if verbose:
        print(f"        compat built in {time.time() - t0:.2f}s", flush=True)

    if verbose:
        print(f"\n[setup] Enumerating all size-4 antichains in Δ(𝔪⁺)...",
              flush=True)
    t0 = time.time()
    antichains = enumerate_antichains_of_size(compat, 4)
    if verbose:
        print(f"        enumerated {len(antichains)} antichains in "
              f"{time.time() - t0:.2f}s", flush=True)

    return {
        "f_idx": f_idx, "f_val": f_val,
        "pos_roots": pos_canonical, "all_roots": all_roots,
        "m_basis": m_basis, "h_basis": h_basis,
        "V_A": V_A, "r_sq": r_sq,
        "m_plus": m_plus, "compat": compat,
        "m_pos_idx_in_canonical": m_pos_idx_in_canonical,
        "rl_set": rl_set,
        "antichains": antichains,
    }


