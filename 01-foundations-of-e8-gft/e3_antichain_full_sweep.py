"""Full 630-antichain verdict sweep for EIX = E_8/(E_7 x SU(2)).

Numerical support for Remark ``rem:antichain-equivariance'' in
``sections/06-emergent-spacetime.tex'': the operational verdicts of
sub-claims (alpha), (gamma), (epsilon) of Hypothesis~\\ref{hyp:4d-emergence}
are constant across the full 630-element set of maximal antichains in
Delta(m_EIX^+) (Corollary~\\ref{cor:antichain-EIX}), without invoking
analytic transitivity of the residual E_7 x SU(2) action.

Per-antichain protocol.
=======================

For each size-4 strongly-orthogonal subset
{alpha_0, ..., alpha_3} subset Delta(m^+) the script forms the abelian
flat a subset m generated by

    X_mu := E_{+alpha_mu} + E_{-alpha_mu}    (mu = 0,1,2,3),

and runs eight checks:

  C1  Strong-orthogonality of the six pairs (alpha_a, alpha_b), a < b.
  C2  Cartan involution: P_h X_mu = 0, P_m X_mu = X_mu (X_mu in m).
  C3  Pairwise commutation: max ||[X_mu, X_nu]|| < 1e-9
       (sub-claim (epsilon) of Hypothesis~\\ref{hyp:4d-emergence} on a).
  C4  Restricted Killing form: kappa|_a = 4 . 1 (positive definite on a).
  C5  Slow-mode kinetic matrix K^{mu nu} = (1/2) delta^{mu nu}
       (Eq.~\\eqref{eq:slow-mode-kinetic}; Riemannian signature on a).
  C6  Osterwalder-Schrader reflection positivity of the Yukawa kernel
       G_2(R) = (m/4 pi^2 R) K_1(m R) on N=30 random points in {tau > 0}
       (Proposition~\\ref{prop:leading-os-rp}).
  C7  O(4) gauge invariance of C6 across the four time-axis choices in a
       (E1 covariance in Proposition~\\ref{prop:gamma-global-poincare}).
  C8  Lorentzian dispersion E^2 = |k|^2 + m^2 > 0 with group velocity
       v_g < 1 on the Wick-rotated propagator (causal propagation).

Test points for C6/C7 are seeded by ``RNG_BASE_SEED + antichain_index``,
so the protocol exercises 630 x (1 + 4) = 3 150 independent OS matrices.

Expected structural outcome.
============================

The kinetic and mass coefficients K^{mu nu} = (1/||X_mu||^2) delta^{mu nu}
and M_W^2 = c_H . r_*^2 depend only on the common length |alpha_mu|^2 = 2
of long E_8 roots and on V_A in Cartan(SU(2)); both are antichain-
independent by construction, so C5--C8 are theoretically constant on
the 630-element set. Strong orthogonality forces [X_mu, X_nu] = 0 on
the Chevalley basis (Knapp 2002, Ch.~II~\\S5; cited at
Proposition~\\ref{prop:algebraic-4d}); C3 is verified numerically on
every antichain as a robustness check.

References.
===========

  - sections/06-emergent-spacetime.tex:
      Proposition~\\ref{prop:algebraic-4d},
      Remark~\\ref{rem:antichain-equivariance},
      Proposition~\\ref{prop:leading-os-rp},
      Proposition~\\ref{prop:gamma-global-poincare}.
  - e3_orbit_selection.py    (enumeration of the 630 maximal antichains).
  - k3b_volovik_signature.py (X_mu construction, K^{mu nu} kinetic matrix).
  - k3b_c1_os_reflection_positivity.py (build_RP_matrix, random points).

Usage.
======

    python3 e3_antichain_full_sweep.py
    python3 e3_antichain_full_sweep.py --save sweep.json
    python3 e3_antichain_full_sweep.py --quick      # first 20 antichains

Optional ``--save`` writes a 630-row JSON table with one record per
antichain.
"""

