def run_sweep(antichains: list,
              m_plus: np.ndarray,
              root_lookup: set,
              m_pos_idx_in_canonical: np.ndarray,
              m_basis: np.ndarray,
              h_basis: np.ndarray,
              f_idx: np.ndarray,
              f_val: np.ndarray,
              r_sq: float,
              n_test_points: int,
              progress_every: int = 50,
              ) -> list[dict]:
    rows = []
    n_total = len(antichains)
    t0 = time.time()
    for idx, ac in enumerate(antichains):
        row = per_antichain_verdict(
            idx=idx, antichain=ac,
            m_plus=m_plus, root_lookup=root_lookup,
            m_pos_idx_in_canonical=m_pos_idx_in_canonical,
            m_basis=m_basis, h_basis=h_basis,
            f_idx=f_idx, f_val=f_val,
            r_sq=r_sq, n_test_points=n_test_points,
        )
        rows.append(row)
        if (idx + 1) % progress_every == 0 or idx + 1 == n_total:
            n_pass = sum(int(r["all_pass"]) for r in rows)
            elapsed = time.time() - t0
            print(f"    {idx + 1:4d}/{n_total}  PASS so far: {n_pass}  "
                  f"({elapsed:6.1f}s, {1000 * elapsed / (idx + 1):.1f} ms/antichain)",
                  flush=True)
    return rows


def _check_distribution(rows: list[dict], key: str,
                        res: Result, label: str) -> None:
    n_total = len(rows)
    n_pass = sum(int(r[key]) for r in rows)
    res.report(
        f"{label}: PASS on all {n_total} antichains",
        n_pass == n_total,
        f"{n_pass}/{n_total} PASS"
        + (f" (FAIL on antichain indices "
           f"{[r['index'] for r in rows if not r[key]][:5]}"
           f"{'...' if (n_total - n_pass) > 5 else ''})"
           if n_pass < n_total else ""),
    )


def _check_constant(rows: list[dict], key: str, expected: float,
                    tol: float, res: Result, label: str) -> None:
    values = np.array([r[key] for r in rows], dtype=np.float64)
    max_dev = float(np.max(np.abs(values - expected)))
    std_val = float(np.std(values))
    res.report(
        f"{label}: constant across all {len(rows)} antichains "
        f"(expected {expected})",
        max_dev < tol,
        f"max |dev| = {max_dev:.3e}, std = {std_val:.3e}",
    )


def report_aggregate(rows: list[dict], res: Result,
                     enforce_full_count: bool = True) -> None:
    banner("[Aggregate] Verdict distribution across the 630-element antichain set")

    n_total = len(rows)
    print(f"    Total antichains processed: {n_total}")
    print(f"    Expected: {EXPECTED_ANTICHAIN_COUNT} "
          f"(Corollary cor:antichain-EIX; cf. e3_orbit_selection.py)")

    label = (f"Antichain count = {EXPECTED_ANTICHAIN_COUNT} "
             f"(consistency with Corollary cor:antichain-EIX)")
    if enforce_full_count:
        res.report(
            label,
            n_total == EXPECTED_ANTICHAIN_COUNT,
            f"counted {n_total}",
        )
    else:
        res.skip(
            label,
            f"sub-set sweep ({n_total} of {EXPECTED_ANTICHAIN_COUNT}); "
            f"--quick mode bypasses the full-count check",
        )

    # Per-check PASS distribution.
    _check_distribution(rows, "C1_pass", res,
                        "C1 strong-orthogonality")
    _check_distribution(rows, "C2_pass", res,
                        "C2 Cartan involution X_μ ∈ 𝔪")
    _check_distribution(rows, "C3_pass", res,
                        "C3 pairwise commutation [X_μ, X_ν] = 0")
    _check_distribution(rows, "C4_pass", res,
                        "C4 κ|_𝔞 = 4·𝟙 (positive)")
    _check_distribution(rows, "C5_pass", res,
                        "C5 K^{μν} signature (+,+,+,+)")
    _check_distribution(rows, "C6_pass", res,
                        "C6 OS reflection positivity (time-axis 0)")
    _check_distribution(rows, "C7_pass", res,
                        "C7 O(4) isotropy: OS-RP across 4 time axes")
    _check_distribution(rows, "C8_pass", res,
                        "C8 Lorentz dispersion E² > 0, v_g < 1")
    _check_distribution(rows, "all_pass", res,
                        "Overall verdict (all 8 checks)")

    # Constancy of structural numerical values.
    _check_constant(rows, "C5_K_diag_mean", 0.5, TOL_ALG, res,
                    "K^{μν}_diag = 1/2")
    _check_constant(rows, "M_W_sq", 0.5, TOL_ALG, res,
                    "M_W² = c_H · r_*² = 1/2")
    _check_constant(rows, "m_eff", 1.0, TOL_ALG, res,
                    "Effective mass m_eff = 1")

    # Min eigenvalue of OS-RP matrix: structural lower bound.
    min_eigs_C6 = np.array([r["C6_min_eig"] for r in rows])
    print(f"    C6 OS-RP min eigenvalue across {n_total} antichains:")
    print(f"      min = {float(min_eigs_C6.min()):+.3e}, "
          f"max = {float(min_eigs_C6.max()):+.3e}, "
          f"median = {float(np.median(min_eigs_C6)):+.3e}")
    res.report(
        "C6 OS-RP min eigenvalue is non-negative (within TOL_PSD) on every antichain",
        bool(min_eigs_C6.min() > -TOL_PSD),
        f"global min = {float(min_eigs_C6.min()):+.3e} > -{TOL_PSD:.0e}",
    )

    max_bracket = np.array([r["C3_max_bracket_norm"] for r in rows])
    print(f"    C3 max ‖[X_μ, X_ν]‖ across {n_total} antichains:")
    print(f"      max = {float(max_bracket.max()):.3e}, "
          f"median = {float(np.median(max_bracket)):.3e}")


