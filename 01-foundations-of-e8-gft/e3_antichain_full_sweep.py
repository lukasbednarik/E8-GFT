def per_antichain_verdict(idx: int,
                          antichain: tuple,
                          m_plus: np.ndarray,
                          root_lookup: set,
                          m_pos_idx_in_canonical: np.ndarray,
                          m_basis: np.ndarray,
                          h_basis: np.ndarray,
                          f_idx: np.ndarray,
                          f_val: np.ndarray,
                          r_sq: float,
                          n_test_points: int = N_TEST_POINTS,
                          ) -> dict:
    """Run the 8-check verdict battery on one size-4 antichain.

    Returns a dict with PASS/FAIL flags and the numerical residuals;
    aggregated across 630 calls into the sweep summary.
    """
    out: dict = {"index": idx, "antichain": list(antichain)}

    # ── C1: strong-orthogonality of all six pairs ────────────────────
    so_pairs = []
    for a in range(4):
        for b in range(a + 1, 4):
            so_pairs.append(
                is_strongly_orthogonal(m_plus[antichain[a]],
                                       m_plus[antichain[b]],
                                       root_lookup)
            )
    out["C1_strong_orth_pairs"] = int(sum(so_pairs))
    out["C1_pass"] = (sum(so_pairs) == 6)

    # ── X_μ basis and slow-mode field basis ─────────────────────────
    X = build_X_mu(m_pos_idx_in_canonical, antichain)
    E_field = slow_mode_field_basis(m_pos_idx_in_canonical, antichain)

    # ── C2: Cartan involution σ → X_μ ∈ 𝔪 ──────────────────────────
    P_m = m_basis.T @ m_basis
    P_h = h_basis.T @ h_basis
    proj_m_residual = float(np.linalg.norm(P_m @ X.T - X.T))
    proj_h_norm = float(np.linalg.norm(P_h @ X.T))
    out["C2_proj_m_residual"] = proj_m_residual
    out["C2_proj_h_norm"] = proj_h_norm
    out["C2_pass"] = (proj_m_residual < TOL_ALG and proj_h_norm < TOL_ALG)

    # ── C3: pairwise commutation [X_μ, X_ν] = 0 ─────────────────────
    max_bracket_norm = 0.0
    for mu in range(4):
        for nu in range(mu + 1, 4):
            br = bracket_vec_fast(f_idx, f_val, X[mu], X[nu])
            n = float(np.linalg.norm(br))
            if n > max_bracket_norm:
                max_bracket_norm = n
    out["C3_max_bracket_norm"] = max_bracket_norm
    out["C3_pass"] = (max_bracket_norm < TOL_ALG)

    # ── C4: restricted Killing form κ|_𝔞 = 4·𝟙 ─────────────────────
    K_a = KAPPA_OVER_EUCLID * (X @ X.T)  # (4, 4)
    K_a_diag = np.diag(K_a)
    K_a_off = K_a - np.diag(K_a_diag)
    K_a_off_max = float(np.max(np.abs(K_a_off)))
    K_a_eigs = np.sort(np.linalg.eigvalsh(K_a))
    out["C4_kappa_diag"] = K_a_diag.tolist()
    out["C4_kappa_off_max"] = K_a_off_max
    out["C4_kappa_min_eig"] = float(K_a_eigs[0])
    out["C4_pass"] = (
        K_a_off_max < TOL_ALG
        and np.allclose(K_a_diag, 4.0, atol=TOL_ALG)
        and K_a_eigs[0] > 0
    )

    # ── C5: slow-mode K^{μν} = (1/2)·𝟙 → signature (+,+,+,+) ────────
    K_munu = slow_mode_kinetic_matrix(X, E_field)
    K_eigs = np.sort(np.linalg.eigvalsh(K_munu))
    K_n_pos = int(np.sum(K_eigs > +TOL_ALG))
    K_n_neg = int(np.sum(K_eigs < -TOL_ALG))
    K_diag_mean = float(np.mean(np.diag(K_munu)))
    out["C5_K_diag_mean"] = K_diag_mean
    out["C5_K_eigs"] = K_eigs.tolist()
    out["C5_signature"] = [K_n_pos, K_n_neg, 4 - K_n_pos - K_n_neg]
    out["C5_pass"] = (K_n_pos == 4 and K_n_neg == 0
                      and abs(K_diag_mean - 0.5) < TOL_ALG)

    # ── effective mass for OS-RP ─────────────────────────────────────
    M_W_sq = C_H_EIX * r_sq  # = 1/4 · 2 = 1/2
    m_eff = float(np.sqrt(M_W_sq / K_diag_mean))
    out["m_eff"] = m_eff
    out["M_W_sq"] = M_W_sq

    # ── C6: OS reflection positivity (single time-axis = 0) ─────────
    rng = np.random.default_rng(RNG_BASE_SEED + idx)
    pts = random_points_positive_time(n_test_points, time_axis=0, rng=rng)
    M = build_RP_matrix(pts, m_eff, time_axis=0)
    sym_err = float(np.linalg.norm(M - M.T))
    eigs_M = np.linalg.eigvalsh(0.5 * (M + M.T))
    eigs_M.sort()
    M_min_eig = float(eigs_M[0])
    M_max_eig = float(eigs_M[-1])
    M_n_neg = int(np.sum(eigs_M < -TOL_PSD))
    out["C6_sym_err"] = sym_err
    out["C6_min_eig"] = M_min_eig
    out["C6_max_eig"] = M_max_eig
    out["C6_n_neg"] = M_n_neg
    out["C6_pass"] = (sym_err < TOL_ALG and M_n_neg == 0)

    # ── C7: O(4) gauge invariance of OS-RP across 4 time axes ───────
    min_eigs_per_axis = []
    n_neg_per_axis = []
    for ta in range(4):
        rng_ta = np.random.default_rng(RNG_BASE_SEED + idx + 100_000 * (ta + 1))
        pts_ta = random_points_positive_time(n_test_points, time_axis=ta,
                                              rng=rng_ta)
        M_ta = build_RP_matrix(pts_ta, m_eff, time_axis=ta)
        eigs_ta = np.linalg.eigvalsh(0.5 * (M_ta + M_ta.T))
        min_eigs_per_axis.append(float(eigs_ta.min()))
        n_neg_per_axis.append(int(np.sum(eigs_ta < -TOL_PSD)))
    out["C7_min_eigs_per_axis"] = min_eigs_per_axis
    out["C7_n_neg_per_axis"] = n_neg_per_axis
    out["C7_pass"] = all(ne == 0 for ne in n_neg_per_axis)

    # ── C8: Lorentz dispersion E² = |k|² + m² > 0, v_g < 1 ──────────
    k_samples = np.array([0.0, 0.5, 1.0, 2.0, 5.0])
    E_sq = k_samples ** 2 + m_eff ** 2
    E = np.sqrt(E_sq)
    rest_mass = float(E[0])
    v_g = k_samples[1:] / E[1:]
    out["C8_rest_mass"] = rest_mass
    out["C8_max_vg"] = float(np.max(v_g))
    out["C8_pass"] = bool(
        np.all(E_sq > 0)
        and abs(rest_mass - m_eff) < TOL_ALG
        and np.all(v_g < 1.0)
    )

    # ── overall verdict ────────────────────────────────────────────
    out["all_pass"] = all(
        out[k] for k in (
            "C1_pass", "C2_pass", "C3_pass", "C4_pass",
            "C5_pass", "C6_pass", "C7_pass", "C8_pass",
        )
    )
    # Coerce all numpy bool / numpy scalar types to JSON-native equivalents.
    for k, v in list(out.items()):
        if isinstance(v, (np.bool_,)):
            out[k] = bool(v)
        elif isinstance(v, (np.integer,)):
            out[k] = int(v)
        elif isinstance(v, (np.floating,)):
            out[k] = float(v)
    return out


